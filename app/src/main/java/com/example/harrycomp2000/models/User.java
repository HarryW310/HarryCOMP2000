package com.example.harrycomp2000.models;

public class User {
    private int id;
    private String username;
    private String email;
    private String role;

    public int getId() {
        return id;
    }

    public String getUsername() {
        return username;
    }

    public String getEmail() {
        return email;
    }

    public String getRole() {
        return role;
    }
}
